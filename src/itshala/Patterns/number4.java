package itshala.Patterns;

public class number4 {
    public static void main(String[] args) {

        int row = 5,k=5;
        for(int i=0; i<row; i++){
            k=5;
            for(int j=0;j<=i;j++){

                System.out.print(k++ +" ");
            }
            System.out.println();

        }

    }


}
